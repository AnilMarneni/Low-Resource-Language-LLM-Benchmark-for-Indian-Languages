import sys
import os
import streamlit as st
import pandas as pd

# Add src to path for direct Streamlit execution
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), "../../")))
from src.visualization.plots import load_latest_benchmark_csv, plot_task_performance, plot_language_breakdown

def main():
    st.set_page_config(page_title="Indic LLM Benchmark Dashboard", page_icon="📊", layout="wide")
    
    st.title("📊 Low-Resource Indic Languages LLM Benchmark")
    st.markdown("Interactive dashboard analyzing Large Language Model performance on Indian Low-Resource Languages.")

    df = load_latest_benchmark_csv(results_dir="results/benchmarks")
    
    if df is None or df.empty:
        st.warning("No benchmark results found. Please run the benchmark framework first!")
        return
        
    st.sidebar.header("Dashboard Controls")
    view_mode = st.sidebar.radio("View Mode:", ["Overview", "Task Analysis", "Language Analysis"])
    
    # Metrics display
    st.header("Overall Run Information")
    col1, col2, col3 = st.columns(3)
    col1.metric("Models Evaluated", len(df["Model"].unique()))
    col2.metric("Languages Tested", len(df["Language"].unique()))
    col3.metric("Tasks Completed", len(df["Task"].unique()))
    
    st.divider()

    if view_mode == "Overview":
        st.subheader("Raw Benchmark Data")
        st.dataframe(df, use_container_width=True)
        
    elif view_mode == "Task Analysis":
        st.subheader("Task-Specific Performance")
        task_choice = st.sidebar.selectbox("Select Task:", df["Task"].unique())
        
        fig = plot_task_performance(df, task_choice)
        if fig:
            st.plotly_chart(fig, use_container_width=True)
        else:
            st.info(f"Not enough data to plot {task_choice}.")
            
    elif view_mode == "Language Analysis":
        st.subheader("Language-Specific Capabilities")
        lang_choice = st.sidebar.selectbox("Select Language:", df["Language"].unique())
        
        fig = plot_language_breakdown(df, lang_choice)
        if fig:
            st.plotly_chart(fig, use_container_width=True)
        else:
            st.info(f"Not enough metric overlap to plot {lang_choice}.")

    st.sidebar.markdown("---")
    st.sidebar.markdown("Generated by the AI Benchmarking Framework.")

if __name__ == "__main__":
    main()
